/*va a tener clases, y lso que tienen clases empieza con mayusculas*/
class Calculadora {
  /*Metodo suma*/
  sumar(num1, num2) {
    return num1 + num2;
  }

  /*Metodo restar*/
  restar(num1, num2) {
    return num1 - num2;
  }

  /*Metodo dividir*/
  dividir(num1, num2) {
    return num1 / num2;
  }

  /*Metodo multiplicar*/
  multiplicar(num1, num2) {
    return num1 * num2;
  }
}
